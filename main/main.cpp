#define IMG_W  234
#define IMG_H  46
#define IMG_WB 30

extern "C" void app_main(void)
{
    printf("License Plate Demo\r\n");
    if (DEV_Module_Init() != 0) {
        return;
    }

    printf("e-Paper Init...\r\n");
    EPD_2IN9G_Init();
    EPD_2IN9G_Clear(EPD_2IN9G_WHITE);
    DEV_Delay_ms(2000);

    UBYTE *BlackImage;
    UDOUBLE Imagesize = ((EPD_2IN9G_WIDTH % 4 == 0) ? (EPD_2IN9G_WIDTH / 4) : (EPD_2IN9G_WIDTH / 4 + 1)) * EPD_2IN9G_HEIGHT;
    if ((BlackImage = (UBYTE *)malloc(Imagesize)) == NULL) {
        printf("malloc failed\r\n");
        return;
    }
    Paint_NewImage(BlackImage, EPD_2IN9G_WIDTH, EPD_2IN9G_HEIGHT, 90, EPD_2IN9G_WHITE);
    Paint_SetScale(4);

    // Yellow background
    Paint_Clear(EPD_2IN9G_YELLOW);

    // Draw white text (foreground pixels only)
    int x0 = (EPD_2IN9G_HEIGHT - IMG_W) / 2;
    int y0 = (EPD_2IN9G_WIDTH - IMG_H) / 2;
    for (int y = 0; y < IMG_H; y++) {
        for (int x = 0; x < IMG_W; x++) {
            if (LicenseImage[y * IMG_WB + x / 8] & (0x80 >> (x % 8))) {
                Paint_SetPixel(x0 + x, y0 + y, EPD_2IN9G_WHITE);
            }
        }
    }

    printf("EPD_Display\r\n");
    EPD_2IN9G_Init_Fast();
    EPD_2IN9G_Display(BlackImage);

    printf("Goto Sleep...\r\n");
    EPD_2IN9G_Sleep();
    free(BlackImage);
    DEV_Delay_ms(2000);
    DEV_Module_Exit();

    printf("Done.\r\n");
    while (1) {
        vTaskDelay(pdMS_TO_TICKS(10000));
    }
}
